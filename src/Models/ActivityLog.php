<?php

namespace Icso\Accounting\Models;
use Illuminate\Database\Eloquent\Model;
class ActivityLog extends Model
{
    public $timestamps = false;
    protected $table = 'als_activity_logs';

    protected $fillable = [
        'user_id',
        'action',
        'model_type',
        'model_id',
        'old_values',
        'new_values',
        'request_payload',
        'ip_address',
        'user_agent',
        'created_at',
    ];

    protected $casts = [
        'old_values' => 'array',
        'new_values' => 'array',
        'request_payload' => 'array',
    ];
}